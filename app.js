let day= "monday";

switch(day) {
    case "monday": 
        console.log("Its monday");
        break;
    case "tuesday":
        console.log("Its tuesday");
        break;
    case "wednesday":
        console.log("Its wednesday");
        break;
    case "thursday":
        console.log("Its thursday");
        break;
    case "friday":    
        console.log("Its friday");
        break;                                          
    case "saturday":
        console.log("Its saturday");   
        break;
    case "sunday":
        console.log("Its sunday");
        break;
    default:
        console.log("Invalid day");
}